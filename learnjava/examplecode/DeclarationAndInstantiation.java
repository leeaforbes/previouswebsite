
public class DeclarationAndInstantiation {
	public static void main(String[] args){
		//declaration: telling the computer "here is a variable type and its name"
		
		//whole numbers
		int wholeNumber;
		
		//decimal numbers
		double number;
		
		//true or false
		boolean isAlive;
		
		//character represents just one letter or number
		char letter;
		
		//multiple characters together
		String word;
		
		
		//instantiation: telling the computer: "a declared variable has this value"
		wholeNumber = 35;
		number = 6.7;
		isAlive = true;
		letter = 'A';
		word = "aBcd blah";
		
		
		//shorthand: combine the two! WOW!
		int countDogs = 2;
		double bankBalance = 2.65;
		boolean isHydrated = false;
		char aGoodLetter = 'u';
		String myName = "Lee";
	}
}
