
public class MorePrinting {
	public static void main(String[] args){
		//the println command takes in a String, which will be talked about in the next lesson
		System.out.println("this is a test");
		
		//blank line
		System.out.println();
		
		//not used as often but still useful
		//print on one line, but with multiple lines of code
		System.out.print("My ");
		System.out.print("name ");
		System.out.print("is ");
		System.out.print("Lee");
	}
}
